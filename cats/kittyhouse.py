class Cat:
    def __init__(self, name, sex, age, id):
        self.name = name
        self.sex = sex
        self.age = age
        self.id = id

    def getName(self):
        return self.name

    def getSex(self):
        return self.sex

    def getAge(self):
        return self.age

    def getID(self):
        return self.id